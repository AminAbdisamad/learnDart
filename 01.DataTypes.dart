import 'dart:math';

/* 
This section contains : 
1. Numbers - Int and double 
2. Strings
3.Booleans (bool)
*/
void main() {
// Numbers - there are two types of numbers 1. int 2. double
  num ab = 2;
  num ac = 12.5;
  num az;
  az = ab + ac;
  print(az);

  // Int
  int x, y, z;
  x = 10;
  y = 20;
  z = x + y;
  print(z);

  // Double
  double a, b, c;
  a = 2.0;
  b = 10.0;
  c = pow(a, b);
  print(c);
// Strings
// Booleans
}
